let x = 10;
let y = 11;

// arithmetic 
console.log(x+y);
//x+y is an expression.
// An expression is a piece of code that produce a value.
console.log(x-y);
console.log(x/y);
console.log(x*y);
console.log(x%y); // modulo
console.log(x**y); // exponantiel

// incrementation
console.log(x++); // 10
//another behavior
console.log(++x); // 11

// decrimentation
console.log(x--); // 10
//another behavior
console.log(--x); // 9